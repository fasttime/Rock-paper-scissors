package rockpaperscissors.logic;

/**
 * The result of a round of rock-paper-scissors.
 */
public enum RoundResult {

    /**
     * The round is won by the first player.
     */
    PLAYER1_WINS,

    /**
     * The round is won by the second player.
     */
    PLAYER2_WINS,

    /**
     * The round ends in a draw.
     */
    DRAW
}
